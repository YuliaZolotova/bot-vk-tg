from __future__ import annotations
from typing import Optional

# –ó–¥–µ—Å—å ‚Äî –ø—Ä–æ—Å—Ç—ã–µ –æ—Ç–≤–µ—Ç—ã –Ω–∞ –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞.
# –†–∞—Å—à–∏—Ä—è–π —Å–ø–∏—Å–æ–∫ –∫–∞–∫ —Ö–æ—á–µ—à—å ‚Äî –æ—Å—Ç–∞–ª—å–Ω–æ–π –∫–æ–¥ —Ç—Ä–æ–≥–∞—Ç—å –Ω–µ –Ω—É–∂–Ω–æ.

KEYWORD_REPLIES = {
    # –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è
    "–ø—Ä–∏–≤–µ—Ç": "–ü—Ä–∏–≤–µ—Ç! üòÑ",
    "–∑–¥—Ä–∞–≤—Å—Ç–≤": "–ü—Ä–∏–≤–µ—Ç! üòÑ",
    "—Ö–∞–π": "–•–∞–π! üòÑ",
    "–¥–æ–±—Ä—ã–π": "–ü—Ä–∏–≤–µ—Ç! üòÑ",

    # –±–∞–∑–æ–≤—ã–µ
    "–∫–∞–∫ –¥–µ–ª–∞": "–ù–æ—Ä–º–∞–ª—å–Ω–æ! –ê —É —Ç–µ–±—è –∫–∞–∫? üôÇ",
    "–∫—Ç–æ —Ç—ã": "–Ø –±–æ—Ç. –ú–æ–≥—É: –∫–∞—Ä—Ç–∞ –¥–Ω—è, –ª—É–Ω–Ω—ã–π –¥–µ–Ω—å, –≥–æ—Ä–æ—Å–∫–æ–ø, –≤—Ä–µ–º—è üôÇ",
    "—á—Ç–æ —É–º–µ–µ—à—å": "–ö–æ–º–∞–Ω–¥—ã: –∫–∞—Ä—Ç–∞ –¥–Ω—è, –ª—É–Ω–Ω—ã–π –¥–µ–Ω—å, –≥–æ—Ä–æ—Å–∫–æ–ø –¥–ª—è <–∑–Ω–∞–∫>, –≤—Ä–µ–º—è üôÇ",
}

def get_keyword_reply(text: str) -> Optional[str]:
    low = (text or "").lower().strip()
    if not low:
        return None

    for key, reply in KEYWORD_REPLIES.items():
        if key in low:
            return reply
    return None
